package AritmetikOperator

fun main(args: Array<String>) {

    val number1 = 12
    val number2 = 6
    var result: Int

    // toplama işlemi
    result = number1 + number2
    println("$number1 + $number2 = $result")

    // çıkarma işlemi
    result = number1 - number2
    println("$number1 - $number2 = $result")

    // çarpma işlemi
    result = number1 * number2
    println("$number1 * $number2 = $result")

    // bölme işlemi
    result = number1 / number2
    println("$number1 / $number2 = $result")

    // mod işlemi
    result = number1 % number2
    println("$number1 % $number2 = $result")


}